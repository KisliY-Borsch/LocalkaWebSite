<h4 class="mt-2 mb-3">Включение программно отключенной сетевой карты (ethernet-адаптера)</h4>
<ul class="list-rectangle">
  <li>1. В меню Пуск (круглая кнопка в левом нижнем углу экрана) откройте Панель управления, затем в разделе Сеть и Интернет откройте ссылку Просмотр состояния сети и задач.</li>
  <li>2. В левой части окна выберите раздел Изменение параметров адаптера.</li>
  <li>3. В списке подключений найдите нужное Подключение по локальной сети, нажмите на нем правой кнопкой мыши и выберите в меню пункт Включить.</li>

<h6>Для предотвращения отключения сетевой карты в дальнейшем, рекомендуем сделать следующее:</h6>
  <li>1. В меню Пуск (круглая кнопка в левом нижнем углу экрана) откройте Панель управления, затем в разделе Сеть и Интернет откройте ссылку Просмотр состояния сети и задач.</li>
  <li>2. В левой части окна выберите раздел Изменение параметров адаптера.</li>
  <li>3. В списке подключений найдите нужное Local Area Connection, нажмите на нем правой кнопкой мыши и выберите в меню пункт Свойства.</li>
  <li>4. В появившемся окне вверху под названием сетевой карты нажмите кнопку Настроить.</li>
  <li>5. В появившемся окне выберите вкладку Управление электропитанием.</li>
  <li>6. Далее снимите флажок Разрешить отключение этого устройства для экономии энергии.</li>
  <li>7. Нажмите кнопку OK в этом и предыдущем окнах.</li>
</ul>

