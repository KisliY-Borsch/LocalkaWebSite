<h4>Настройка соединения PPPоE</h4>

<ul class="list-rectangle">
<li>1. В меню Пуск (круглая кнопка в левом нижнем углу экрана) откройте Панель управления, затем в разделе Сеть и Интернет откройте ссылку Просмотр состояния сети и задач.</li>
<li>2. В средней части окна в разделе Изменение сетевых параметров откройте ссылку Настройка нового подключения или сети.</li>
<li>3. В списке выберите пункт Подключение к Интернету (Беспроводное, скоростное или телефонное подключение к Интернету.) и нажмите кнопку Далее.</li>
<li>4. После появления диалога Подключение к Интернету уже выполнено в списке выберите Все равно создать новое подключение.</li>
<li>5. В списке выберите Высокоскоростное (с PPPoE).</li>
<li>6. В поля Имя пользователя и Пароль введите имя (логин) и пароль для входа в сеть Интернет соответственно.</li>
<li>7. Для того, чтобы система запомнила пароль и не перезапрашивала его при каждом подключении, установите флажок Запомнить этот пароль (рекомендуется).</li>
<li>8. В поле Имя подключения введите название подключения LOCALKA.</li>
<li>9. Нажмите кнопку Подключить. Если все настройки выполнены правильно, подключение к сети Интернет произойдет сразу.</li>
<li>10. В случае, если подключение все-таки не произошло, в появившемся окне выберите пункт Все равно создать это подключение.</li>
<li>11. В меню Пуск откройте Панель управления, затем в разделе Сеть и Интернет откройте ссылку Просмотр состояния сети и задач.</li>
<li>12. В правой части окна выберите раздел Изменение параметров адаптера. Нажмите правую кнопку мыши на только что созданном подключении LOCALKA.</li>
<li>13. В появившемся меню выберите Создать ярлык. На системный запрос Поместить его на рабочий стол? ответьте Да.</li>
<li>14. Созданное подключение теперь доступно прямо на рабочем столе.</li>
 </ul>

</div>


